"use strict";
(() => {
  const $ = (query) => document.querySelector(query);
  const escapeHtml = (value) => String(value ?? "").replace(/[&<>'"]/g, (character) => ({
    "&": "&amp;", "<": "&lt;", ">": "&gt;", "'": "&#39;", '"': "&quot;"
  }[character]));
  const labels = {
    jss1: "JSS 1", jss2: "JSS 2", jss3: "JSS 3", "ss1-general": "SS 1",
    "ss2-arts": "SS 2 Arts", "ss2-business": "SS 2 Business", "ss2-science": "SS 2 Science",
    "ss3-arts": "SS 3 Arts", "ss3-science": "SS 3 Science"
  };

  function toast(message, type = "default") {
    const node = document.createElement("div");
    node.className = `toast ${type}`;
    node.textContent = String(message || "Request failed.");
    $("#toastContainer").appendChild(node);
    setTimeout(() => node.remove(), 4500);
  }

  async function credentials() {
    const cfg = await window.WTSDashboardConfigReady;
    try { return JSON.parse(localStorage.getItem(cfg.storageKey) || "null"); }
    catch { return null; }
  }

  async function rpc(functionName, action, payload = {}) {
    const cfg = await window.WTSDashboardConfigReady;
    const auth = await credentials();
    if (!auth?.adminCode || !auth?.adminSecret) throw new Error("Administrator connection is not configured.");
    const response = await fetch(`${cfg.supabaseUrl}/rest/v1/rpc/${functionName}`, {
      method: "POST",
      headers: { "Content-Type": "application/json", apikey: cfg.publishableKey },
      body: JSON.stringify({
        p_client_code: auth.adminCode,
        p_client_secret: auth.adminSecret,
        p_action: action,
        p_payload: payload
      })
    });
    const data = await response.json();
    if (!response.ok || data?.ok === false) throw new Error(data?.code || "Report request failed.");
    return data;
  }

  const monitor = (action, payload) => rpc("attendance_monitor_admin_read_api", action, payload);
  const controls = (action, payload) => rpc("attendance_controls_admin_read_api", action, payload);

  function header(title) {
    const from = $("#fromDate").value;
    const to = $("#toDate").value;
    return `<header class="report-head"><p><strong>WAY TO SUCCESS STANDARD SCHOOLS, EJIGBO</strong></p><h1>${escapeHtml(title)}</h1><p>Attendance Management System</p></header><section class="meta"><div><span>Session</span><strong>2026/2027</strong></div><div><span>Period From</span><strong>${escapeHtml(from)}</strong></div><div><span>Period To</span><strong>${escapeHtml(to)}</strong></div><div><span>Generated</span><strong>${new Date().toLocaleString("en-NG")}</strong></div></section>`;
  }

  function footer() {
    return `<footer class="footer"><span>Confidential management document</span><span>Generated by WTS Digital Attendance System</span></footer>`;
  }

  function summaryCards(items) {
    return `<div class="summary-grid">${items.map(([label, value]) => `<div class="summary-card"><span>${escapeHtml(label)}</span><strong>${escapeHtml(value)}</strong></div>`).join("")}</div>`;
  }

  function table(headers, rows) {
    return `<table><thead><tr>${headers.map((item) => `<th>${escapeHtml(item)}</th>`).join("")}</tr></thead><tbody>${rows.join("")}</tbody></table>`;
  }

  async function renderOverview(payload) {
    const [overview, trend, health] = await Promise.all([
      monitor("analyticsOverview", payload),
      monitor("dailyTrend", payload),
      monitor("health", payload)
    ]);
    const students = overview.students || {};
    const staff = overview.staff || {};
    const snapshot = health.snapshot || {};
    const trendRows = (trend.trend || []).map((row) => `<tr><td>${escapeHtml(row.attendance_date)}</td><td>${row.records}</td><td>${row.present}</td><td>${row.late}</td><td>${row.absent}</td><td>${row.excused}</td><td>${row.average_late_minutes}</td></tr>`);
    return header("Executive Attendance Overview") +
      `<h2>Student Summary</h2>${summaryCards([["Records", students.records || 0],["Present", students.present || 0],["Late", students.late || 0],["Absent", students.absent || 0],["Excused", students.excused || 0],["Average Late Minutes", students.average_late_minutes || 0],["Average Time on Premises", students.average_minutes_on_premises || 0],["Excess Minutes", students.total_excess_minutes || 0]])}` +
      `<h2>Staff Summary</h2>${summaryCards([["Records", staff.records || 0],["Present", staff.present || 0],["Late", staff.late || 0],["Absent", staff.absent || 0],["Excused", staff.excused || 0],["Average Late Minutes", staff.average_late_minutes || 0],["Average Worked Minutes", staff.average_worked_minutes || 0],["Open Alerts", (health.open_alerts || []).length]])}` +
      `<h2>Operational Health</h2>${summaryCards([["Expected Students", snapshot.expected_students || 0],["Student Records Today", snapshot.student_records || 0],["Expected Staff", snapshot.expected_staff || 0],["Staff Records Today", snapshot.staff_records || 0],["Active Devices", snapshot.active_devices || 0],["Online Devices", snapshot.online_devices || 0],["Rollout Stage", snapshot.rollout_stage || "—"],["Automation", snapshot.automatic_absence_enabled ? "Enabled" : "Disabled"]])}` +
      `<h2>Daily Trend</h2>${table(["Date","Records","Present","Late","Absent","Excused","Avg Late"], trendRows)}` + footer();
  }

  async function renderClasses(payload) {
    const data = await monitor("classSummary", payload);
    const rows = (data.classes || []).map((row) => `<tr><td>${escapeHtml(labels[row.class_key] || row.class_key)}</td><td>${row.roster_students}</td><td>${row.records}</td><td>${row.present}</td><td>${row.late}</td><td>${row.absent}</td><td>${row.excused}</td><td>${row.average_late_minutes}</td><td>${row.average_minutes_on_premises}</td></tr>`);
    return header("Class Attendance Summary") + `<h2>Class Performance</h2>${table(["Class","Roster","Records","Present","Late","Absent","Excused","Avg Late","Avg Duration"], rows)}` + footer();
  }

  async function renderStudents(payload) {
    const data = await monitor("studentSummary", payload);
    const rows = (data.students || []).map((row) => `<tr><td>${escapeHtml(row.full_name_snapshot)}</td><td>${escapeHtml(row.admission_number_snapshot || "")}</td><td>${escapeHtml(labels[row.class_key] || row.class_key)}</td><td>${escapeHtml(row.roster_status)}</td><td>${row.records}</td><td>${row.present}</td><td>${row.late}</td><td>${row.absent}</td><td>${row.excused}</td><td>${row.total_late_minutes}</td><td>${row.total_excess_minutes}</td></tr>`);
    return header("Student Attendance Summary") + `<h2>Individual Student Records</h2>${table(["Student","Admission No.","Class","Status","Records","Present","Late","Absent","Excused","Late Minutes","Excess Minutes"], rows)}` + footer();
  }

  async function renderStaff(payload) {
    const data = await monitor("staffSummary", payload);
    const rows = (data.staff || []).map((row) => `<tr><td>${escapeHtml(row.full_name)}</td><td>${escapeHtml(row.staff_number || "")}</td><td>${escapeHtml(row.designation || row.department || "")}</td><td>${row.records}</td><td>${row.present}</td><td>${row.late}</td><td>${row.absent}</td><td>${row.excused}</td><td>${row.total_late_minutes}</td><td>${(Number(row.total_worked_minutes || 0) / 60).toFixed(1)}</td></tr>`);
    return header("Staff Attendance Summary") + `<h2>Digital Time-Book Report</h2>${table(["Staff","Staff No.","Designation","Records","Present","Late","Absent","Excused","Late Minutes","Worked Hours"], rows)}` + footer();
  }

  async function renderControls() {
    const data = await controls("summary");
    const counts = data.counts || {};
    const readiness = data.latest_readiness || {};
    const backup = data.latest_backup || {};
    const checkRows = (readiness.checks || []).map((check) => `<tr><td>${escapeHtml(check.code)}</td><td>${escapeHtml(check.status)}</td><td>${escapeHtml(check.detail || check.count || "")}</td></tr>`);
    return header("Attendance Governance and Readiness Report") +
      `<h2>Control Queue</h2>${summaryCards([["Pending Corrections", counts.pending_corrections || 0],["Pending Manual Entries", counts.pending_manual_entries || 0],["Guardian Contacts", counts.active_guardians || 0],["Draft Notifications", counts.draft_notifications || 0],["Queued Notifications", counts.queued_notifications || 0],["Active Administrators", counts.admin_clients || 0],["Backup Manifests", counts.backup_manifests || 0],["Readiness Checks", counts.readiness_checks || 0]])}` +
      `<h2>Latest Deployment Readiness</h2>${summaryCards([["Overall Status", readiness.overall_status || "Not run"],["Environment", readiness.environment || "—"],["Session", readiness.academic_session || "—"],["Rollout", readiness.rollout_stage || "—"]])}${table(["Check","Status","Detail"], checkRows)}` +
      `<h2>Latest Backup Manifest</h2>${summaryCards([["Type", backup.manifest_type || "Not created"],["Status", backup.status || "—"],["Checksum", backup.checksum_sha256 || "—"],["Completed", backup.completed_at ? new Date(backup.completed_at).toLocaleString("en-NG") : "—"]])}` + footer();
  }

  async function loadReport() {
    const payload = {
      session: "2026/2027",
      from: $("#fromDate").value,
      to: $("#toDate").value,
      classKey: $("#classKey").value
    };
    try {
      const type = $("#reportType").value;
      let html;
      if (type === "overview") html = await renderOverview(payload);
      if (type === "classes") html = await renderClasses(payload);
      if (type === "students") html = await renderStudents(payload);
      if (type === "staff") html = await renderStaff(payload);
      if (type === "controls") html = await renderControls();
      $("#report").innerHTML = html;
      toast("Report loaded and ready to print.", "success");
    } catch (error) { toast(error.message, "error"); }
  }

  async function openConnection() {
    const cfg = await window.WTSDashboardConfigReady;
    const auth = await credentials();
    $("#adminCode").value = auth?.adminCode || "";
    $("#adminSecret").value = auth?.adminSecret || "";
    $("#connectionDialog").showModal();
  }

  async function saveConnection(event) {
    event.preventDefault();
    const cfg = await window.WTSDashboardConfigReady;
    localStorage.setItem(cfg.storageKey, JSON.stringify({
      adminCode: $("#adminCode").value.trim(),
      adminSecret: $("#adminSecret").value.trim()
    }));
    $("#connectionDialog").close();
    loadReport();
  }

  const now = new Date();
  $("#toDate").value = new Date(now.getTime() - now.getTimezoneOffset() * 60000).toISOString().slice(0, 10);
  const monthAgo = new Date(now.getTime() - 30 * 86400000);
  $("#fromDate").value = new Date(monthAgo.getTime() - monthAgo.getTimezoneOffset() * 60000).toISOString().slice(0, 10);
  $("#loadReport").addEventListener("click", loadReport);
  $("#printReport").addEventListener("click", () => window.print());
  $("#connectButton").addEventListener("click", openConnection);
  $("#connectionForm").addEventListener("submit", saveConnection);
})();
